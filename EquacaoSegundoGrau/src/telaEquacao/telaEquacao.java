    private void btnCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcActionPerformed
        // TODO add your handling code here:
        pnlResultado.setVisible(true);
        int a = Integer.parseInt(txtA.getValue().toString());
        int b = Integer.parseInt(txtB.getValue().toString());
        int c = Integer.parseInt(txtC.getValue().toString());
        int a1 = a * 4;
        double b1 = Math.pow(b, 2);
        double delta = b1 - (a1 * c);
        
        lblDelta.setText(String.valueOf(delta));
        double x = ((b * -1)+ Math.sqrt(delta))/(2*a);
        double y = ((b * -1)- Math.sqrt(delta))/(2*a);
        lblRaizX.setText(String.format("%.2f", x));
        lblRaizY.setText(String.format("%.2f",y));
        
        if (delta > 0){
            lblTipoRaiz.setText("Exitem Raizes Reais");
        }else {
            lblTipoRaiz.setText("Nao existem Raizes Reais");
        }
    }//GEN-LAST:event_btnCalcActionPerformed

