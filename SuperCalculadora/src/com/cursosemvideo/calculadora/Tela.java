    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        panCalc.setVisible(true);
        int n1 = Integer.parseInt(valor.getValue().toString());
        int resto = n1%2;
        lblResto.setText(Integer.toString(resto));
        double cubo = Math.pow(n1, 3);
        lblCubo.setText(Double.toString(cubo));
        double raizQuad = Math.sqrt(n1);
        lblQuadrada.setText(String.format("%.2f", raizQuad));//assim fica longo Double.toString(raizQuad));
        double raizCubica = Math.cbrt(n1);
        lblCubica.setText(String.format("%.2f",raizCubica));//Double.toString(raizCubica));
        int absoluto = Math.abs(n1);
        lblAbsoluto.setText(Integer.toString(absoluto));
    }//GEN-LAST:event_btnCalcularActionPerformed

